#include "../includes/lemin.h"

int		ft_is_number(char *num)
{
	int i;

	i = 0;
	while (num[i] != '\0')
	{
		if (!ft_isdigit(num[i]))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
